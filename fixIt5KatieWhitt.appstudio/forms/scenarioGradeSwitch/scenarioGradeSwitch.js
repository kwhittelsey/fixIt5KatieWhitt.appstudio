let grade = prompt("Enter your current letter grade as a capital letter. ");

switch (grade) {
case (grade = "A"):
   alert("Good job");
break;

case (grade = "B"):
   alert("Pretty good");
break;
   
case (grade = "C"):
   alert("Passed");
break;

case (grade = "D"):
   alert("Not so good");
break;

case (grade = "F"):
   alert("Whoops, failed");
break;

default:
    alert("Uh oh");
} 
